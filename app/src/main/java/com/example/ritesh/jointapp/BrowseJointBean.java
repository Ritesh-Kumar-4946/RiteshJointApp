public class BrowseJointBean  {


    private String id;
    private String username;
    private String image;
    private String line_work;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getLine_work() {
        return line_work;
    }

    public void setLine_work(String line_work) {
        this.line_work = line_work;
    }
}
